public class NullArithmetic {

    public static void main(String[] args) {

        Integer number = null;
        Double dNumber = null;

        System.out.println(number);
        System.out.println(dNumber);

        System.out.println(number+1);
        System.out.println(dNumber+"null");

    }

}
